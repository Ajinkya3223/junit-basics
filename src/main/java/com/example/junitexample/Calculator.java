package com.example.junitexample;

public class Calculator {

    public  static int doSum(int a,int b,int c){
        return a+b+c;
    }

    public  static int doProduct(int a, int b, int c){
        return a*b*c;
    }

    public static boolean doCompare(int a,int b){
        return a==b;
    }
}
